<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_0" style="width: 610.88px;">Los braquicéfalos... ¿por qué se ahogan?</h2>
        <div class="apartado">
        <p>Los pug son una <strong>raza de perros conocidas como braquicéfala</strong>. Han sido criados para tener una mandíbula inferior uniforme, una mandíbula superior más compacta, y una cara y nariz cortas y casi totalmente planas. Esta característica que los diferencia y hace especiales entre todas las razas del mundo, los hace muy lindos pero, al mismo tiempo, les produce problemas respiratorios. Todos los cuidadores de pug notan la constancia y la variedad de ruidos que realiza su pequeña mascota. Ellos no respiran correctamente y la mayoría ronca cuando duerme.</p><p>Entre las enfermedades o condiciones respiratorias se encuentra el "ahogamiento", que a partir de este momento, no deberíamos llamarlo de esta forma porque tu perro realmente no se está ahogando. El nombre que le dan los expertos a esta condición es "<strong>estornudo inverso</strong>", el cuál, todos los pug lo sufren de vez en cuando.</p><p>Este problema se expresa casi como una asfixia y es normal ver al perro extender su cuello y arquear la espalda. Si hablamos de forma coloquial, con un estornudo el perro expulsaría el aire con una exhalación, pero con este estornudo inverso ese aire se está inhalando. A medida que el aire entra en el organismo del perro, el pug emite un sonido que puede parecer como una tos pesada o un ronquido profundo con un resoplido. Estos episodios que pueden durar incluso hasta un minuto, vienen y van con bastante rapidez y suceden durante toda la vida del perro.</p><p>El pug y otros perros braquicéfalos tienen un pasaje nasal acortado y un sistema respiratorio elevado que le produce <strong>dificultades para respirar en general</strong>. De igual forma, al poseer este tipo de anatomía, tu perro pug es propenso a la irritación del paladar blando y la garganta. Esta irritación también es una de las causas de este tipo de "estornudo".</p>
