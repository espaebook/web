<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_6">Vacuna contra la leucemia felina</h2>
<p>¿Te preguntas cómo prevenir la leucemia felina? La medida principal consistirá en seguir el calendario de vacunación del gato para inmunizar al felino. Así mismo, vale la pena señalar que la vacunación no suele tener efecto en animales ya infectados. Así pues, es importante que antes de adoptar un gatito nos aseguremos que no tiene la enfermedad. Esta es <strong>la única forma de prevenir la leucemia felina</strong>.</p>
<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_7">Esperanza de vida de un gato con leucemia felina</h2>
<p>¿Cuánto vive un gato con leucemia felina? El pronóstico de la leucemia en gatos es <strong>reservado</strong>. Generalmente se produce una alta mortaldad, sin embargo, podemos ofrecer una buena calidad de vida a los individuos que la padecen, que tendrán una esperanza de vida de <strong>entre 2 y 4 años</strong>. En algunos casos observaremos que los gatos parecen sanos durante algunos años, especialmente en individuos adultos. Por el contrario, la patología se desarrolla muy rápido en cachorros.</p>
