<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_0" style="width: 610.88px;">El virus de la leucemia felina</h2>
<div class="apartado">
<p>¿Qué es la leucemia felina? La enfermedad que conocemos comúnmente como "leucemia en gatos" está provocada por el virus de la leucemia felina (FLVe), un retrovirus que pertenece a la familia Oncovirinae. Los oncovirus provocan diversas afecciones inmunológicas, degenerativas e incluso proliferativas. Pueden ser endógenas o exógenas. En este segundo caso son capaces de replicarse cuando se produce la transmisión de FLVe, como es el caso del virus del sarcoma felino (FeSV), un tipo de tumor maligno que se produce en los tejidos blandos.</p>
<p>Dentro del FLVe encontramos cuatro subgrupos, no obstante, prácticamente todos los felinos infectados han sido contagiados por el FLVe-A. De forma resumida, se caracterizan por:</p>
<p> &bull; FLVe-A: es la forma original del virus, aunque se pueden desarrollar formas mutadas.<br>
    &bull; FeLV-B: predispone al felino a padecer neoplasias (crecimiento anormal del tejido).<br>
    &bull; FeLV-C: asociado al desarrollo de hipoplasia eritroide y anemia grave.<br>
    &bull; FeLV-T: predispone a la infección y destrucción de linfocitos T.</p>
<p>Los subgrupos se pueden detectar a través de las distintas pruebas para el diagnóstico, que mencionaremos más adelante en otro apartado específico. A continuación hablaremos del contagio.</p>
</div>
<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_0">¿Cómo se contagia la leucemia felina?</h2>
<div class="apartado">
<p>El virus de la leucemia felina se contagia principalmente durante el embarazo de la gata, dentro del útero y en el período de lactancia, pero también mediante los fluidos corporales, tales como saliva, orina, sangre o secreciones nasales. El aseo mutuo, el uso de la misma caja de arena o platos de comida, además de las peleas que causan heridas externas sangrantes, exponen a los gatos sanos a contagiarse del virus.</p>
<p>Todos los felinos son susceptibles a contraer el virus de la leucemia felina, no obstante, los cachorros y los gatos jóvenes son los más vulnerables a padecerlo, especialmente cuando hablamos de felinos domésticos que tienen acceso al exterior, principalmente machos no castrados o que padecen otras afecciones (como enfermedades respiratorias, orales y abscesos) o de los gatos sin hogar, como gatos abandonados y gatos ferales.</p>
<p>Cuando el virus entra en contacto con el sistema inmunitario del animal pueden darse tres situaciones:</p>
<ol>
    <li>El gato es inmune y ha creado anticuerpos para hacer frente al virus. Puede mostrar los síntomas característicos de la enfermedad unas semanas que luego remitirán.</li>
    <li>El virus entra en la sangre y la saliva del individuo, dañando el sistema inmunitario y provocando leucemia. En este caso el felino es susceptible a padecer diversas enfermedades. Su esperanza de vida es reducida.</li>
    <li>El virus se elimina a través de la sangre y la saliva pero persiste en la médula ósea. Si bien puede reactivarse, lo más frecuente es que no afecte al felino.</li>
</ol>
<p>Así pues, la leucemia en gatos es contagiosa y especialmente grave, por lo que es muy importante prevenirla y tomar las medidas pertinentes para evitar que nuestro felino la contraiga.</p>
<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_0">¿La leucemia felina se contagia a humanos?</h2>
<p>Una de las mayores preocupaciones de los tutores es saber si la leucemia en gatos se contagia a humanos. Estudios realizados han utilizado diversas cepas del FLVe para cultivarlas en tejidos humanos y demostrar si existía o no un riesgo potencial de transmisión a las personas. De momento, los estudios señalan que no existen riesgos para las personas y no existe ningún caso conocido que pueda demostrar que se trata de una enfermedad zoonótica.</p>
<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_0">Síntomas de leucemia felina</h2>
<p>La leucemia en gatos y sus síntomas son especialmente variados y dependerán en gran medida del estado de salud del propio individuo. Es frecuente observar que padece diversos problemas de salud a la vez, que tiene ciertas dificultades para superarlos y que, de forma progresiva, el estado de salud general del gato empeora.</p>
