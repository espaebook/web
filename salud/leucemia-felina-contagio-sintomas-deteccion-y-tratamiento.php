<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_4">Diagnóstico de leucemia en gatos</h2>
<p>Si has observado uno o diversos de los síntomas mencionados deberás dirigirte a tu veterinario para confirmar la enfermedad mediante el <strong>test de leucemia felina</strong> con un <strong>hemograma</strong>. Es habitual que el especialista recomiende realizar esta prueba tras la adopción, antes de la vacunación, si ha estado expuesto al virus o si presenta algún síntoma anormal. Mediante un simple análisis de sangre podremos determinar la presencia del virus de la leucemia felina en nuestro gato.</p>
<p>Algunas de las pruebas son:</p>
<ul><li><strong>ELISA</strong> (ensayo por inmunoabsorción ligado a enzimas): es típica en las clínicas veterinarias. Se recoge una muestra de sangre del gato y se detecta si en ella hay la presencia del antígeno, o estados iniciales de la infección. Al cabo de varias semanas se confirmará si es positivo, ya que no se sabe si la infección es temporal o permanente.</li><li><strong>PCR</strong> (reacción en cadena de la polimerasa): detecta el ADN del virus en las células afectadas ya sea en muestras de sangre, como en otros tejidos. A pesar de que puede detectar infecciones latentes, no está tan disponible a diferencia de ELISA.</li><li><strong>IFA</strong> (inmunofluorescencia direc): no es útil para detectar estadios iniciales pero sirve para confirmar positivos de ELISA. Detecta la presencia del antígeno en células infectadas.</li></ul>
<p>Es posible que, tras realizar la prueba, el veterinario nos sugiera <strong>realizarla de nuevo pasados 30 días</strong> si ha dado negativo pero el riesgo de infección es alto. Además, si nuestro felino tiene acceso al exterior se aconseja realizar la prueba <strong>anualmente</strong>. En el caso de dar positivo es probable que se soliciten pruebas adicionales.</p>
<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_5">Tratamiento para leucemia felina</h2>
<p>Es muy importante señalar que <strong>la leucemia felina no tiene cura</strong>, no obstante, determinados individuos pueden llevar un estilo de vida positivo si reciben atención veterinaria regular que incluya la administración de <strong>antivirales e inmunoreguladores</strong> que ayuden al felino a obtener cierta protección frente a infecciones secundarias.</p>
<p>También será necesario ofrecer buenos cuidados al gato, que incluyan una dieta específica, un estado de <strong>bienestar generalizado que evite el estrés</strong> y un estado de salud óptimo. En el caso de querer complementar el tratamiento veterinario con un tratamiento natural para la leucemia felina, como vitaminas para gatos con leucemia, consultaremos siempre con el especialista.</p>
<p>Por otro lado, deberemos tomar ciertas precauciones para evitar que nuestro gato pueda contagiar a otros felinos. El veterinario nos sugerirá que <strong>mantengamos al gato en el interior</strong> para reducir el riesgo de propagación, además, valorará la <strong>castración</strong> del individuo para evitar el escapismo derivado de la conducta sexual.</p>
