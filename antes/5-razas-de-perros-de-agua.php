<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_1">2. Barbet</h2>
<div class="apartado">
<p>El <strong>barbet</strong>, también conocido como perro de agua francés, es considerado como el<strong> perro de agua por excelencia, </strong>habiendo participado en la creación no solo del caniche, sino en la de todas las razas de perros de agua que aparecerán en nuestra lista. Es una raza canina muy antigua que se ha originado en Francia y conquistado mucha popularidad en toda Europa durante los siglos XV y XVI. Originalmente fue entrenada para la caza menor, en especial como cobradores de aves acuáticas salvajes.</p><p>De porte mediano, el barbet se caracteriza por el manto<strong> largo, lanoso y rizado</strong>, que crece de forma abundante y puede llegar a formar mechas en los perros adultos. Este pelaje tan llamativo y peculiar requiere un mantenimiento específico para evitar romper la fibra capilar y, a la vez, mantenerlo libre de nudos y suciedades. Respecto a su temperamento, es un can <strong>enérgico y sociable</strong>, que experimenta un vinculo muy especial con sus tutores. También es un amante del agua y eximio nadador, disfrutando enormemente de las actividades acuáticas.</p>
