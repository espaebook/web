<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_3">4. Perro de agua portugués</h2>
<div class="apartado">
<p>El perro de agua portugués, conocido también como<strong> cão portugués</strong>, es un perro mediano y de rasgos armónicos, que demuestra un temperamento muy equilibrado y predispuesto al entrenamiento, además de un estupendo dote para la natación. El estándar oficial de la raza reconoce dos variedades de pelaje en el perro de agua portugués: el largo <strong>ondulado y el rizado</strong>, ambos considerados hipoalergénicos.</p><p>Su historia se entrelaza con la del perro de agua español, pues sus orígenes están asociados a la llegada de los musulmanes a la costa portuguesa, principalmente en la región de <strong>Algarbe</strong>. Además de probablemente compartir poseer antepasados en común, ambas razas fueron influenciadas por cruces con el perro barbet.</p>
