<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_2">3. Perro de agua español</h2>
<div class="apartado">
<p>El perro de agua español es una de las más populares razas de perros originadas en España. Al tratarse de una raza antigua, poco se sabe con exactitud sobre su nacimiento, pero se estima que sus orígenes remeten a la llegada de los musulmanes a la Península Ibérica, en meados del año <strong>711 d.C</strong>. Si bien existen hipótesis que atribuyan su nacimiento a la región de Cantabria (al norte de España), su crianza siempre ha sido especialmente popular en Andalucía, donde es conocido como "Perro Turco".</p><p>En el siglo XVIII, eran ampliamente empleados como perros de trabajo en los campos españoles, desempeñando principalmente funciones de<strong> pastoreo y caza menor</strong>. Pero al percibir sus notables habilidades para nadar, los pescadores del norte de España también empezaron a utilizarlos como ayudantes.</p><p>Con el paso de los años, otras razas fueron sustituyendo a los perros de agua españoles en dichas labores, por lo que <strong>su población ha empezado a decaer</strong>. Afortunadamente, por su versatilidad, energía e inteligencia, el perro de agua español se ha adaptado óptimamente a los deportes caninos, siendo hoy uno de los perros deportistas más queridos en España. </p>
