<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_0" style="width: 610.88px;">1. Caniche o poodle</h2>
<div class="apartado">
<p>Destacando como una de las razas caninas más inteligentes, el caniche o poodle es <strong>el perro de agua más popular del mundo</strong>. Si bien su nacimiento suela atribuirse a Francia, sus orígenes son controvertidos y se estima que el aspecto y comportamiento del poodle tal como los conocemos hoy fue influenciado por perros barbet, que también aparecerán en nuestra lista de razas de perro de agua.</p><p>Actualmente, el caniche o poolde es uno de los perros de compañía más queridos por su <strong>temperamento alegre</strong>, inteligente y versátil, que le permite adaptarse con facilidad a tutores de diferentes personalidades y estilos de vida. Pero en sus comienzos, la raza fue creada para desempeñarse como <strong>perros cobradores</strong>, principalmente de aves acuáticas, por lo que conserva un sentido de presa bien desarrollado. </p>
