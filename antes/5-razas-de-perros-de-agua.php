<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_4">5. Laggotto romagnolo</h2>
<div class="apartado">
<p>El laggotto romagnolo es una raza de perro italiano de tamaño medio, cuyos orígenes y su propio nombre se remontan a la región de <strong>Romagna</strong>. Como buen perro de agua, ha sido originalmente entrenado como perro cobrador de aves acuáticas que habitaban principalmente cerca de zonas pantanosas. Pero posteriormente también se ha revelado un magnifico <strong>detector de trufas</strong> y, por supuesto, un compañero incondicional.</p><p>Su rasgo más destacado es, lógicamente, el manto lanoso, denso y rizado que caracteriza a las razas de perros de agua. Respecto a su carácter, el lagotto romagnolo es un <strong>can enérgico, alerta y leal</strong>, con sentidos bien desarrollados y notable vocación para el trabajo y la natación. Por su gran energía y notable inteligencia, necesitan recibir estimulación diariamente, tanto física como mental, además de contar con un ambiente enriquecido para conservar un <strong>comportamiento equilibrado</strong>.</p>
