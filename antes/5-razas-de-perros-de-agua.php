<p>Las <strong>razas de perros de agua</strong> forman una gran y diversificada familia de canes de tamaño medio que destacan por poseer un manto lanoso y/o rizado, un comportamiento activo y notable vocación para las actividades acuáticas. Históricamente, estos perros estaban asociados a las <strong>labores pesqueras y de caza menor</strong>, desempeñándose principalmente como cobradores de aves y pequeños mamíferos acuáticos.</p></div>
