<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_0" style="width: 610.88px;">¿Qué es el comensalismo?</h2>
<div class="apartado">
<p>El comensalismo en biología se define como la relación entre dos organismos de especies distintas, donde <b>uno de ellos sale beneficiado y el otro no</b><strong> obtiene nada</strong>, ni positivo ni negativo. El resultado de la relación para uno de ellos es neutro.</p><p>El comensalismo es un tipo de simbiosis que, a diferencia de otros, como el parasitismo o la depredación, no resulta en un final negativo para ninguna de las partes implicadas. Por otro lado, la <strong>diferencia entre mutualismo y comensalismo</strong> es que, en el primer caso, ambos individuos obtienen beneficios.</p><p>El comensalismo en microbiología también ha sido ampliamente estudiado. Por ejemplo, en una columna de agua, los microorganismos que viven más cerca de la superficie suelen beneficiarse de la luz solar, indispensable para su desarrollo. Sus sustancias de desechos viajan por la columna de agua hasta llegar al fondo, donde escasea la luz solar y el oxígeno. Aquí, <strong>microorganismo anaerobios</strong> (que no necesitan oxígeno para vivir) toman como fuente de nutrientes y energía las sustancias que llegan desde la superficie.</p><p>Los microorganismos del fondo se benefician de los microorganismos de superficies, mientras éstos no obtienen nada. Cabe destacar aquí el término <strong>amensalismo</strong>. Contrario al comensalismo, en estas relaciones una de las partes sale perjudicada mientras la otra permanece inmutable. Es el caso de ciertos hongos como <i>Penicillium</i>, que segregan antibióticos impidiendo la proliferación de bacterias.</p>
