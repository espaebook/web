<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_1">Tipos de comensalismo</h2>
<div class="apartado">
<p>Al estudiar las relaciones que se establecen entre los seres vivos, la gran variedad existente nos obliga a subclasificar el comensalismo en tres tipos diferentes, pues no existe una única vía en la que se beneficien los animales del mutualismo:</p><ul><li><strong>Foresis</strong>: el término foresis hace referencia a la relación que se establece entre dos especies cuando una de ellas transporta a la otra. En la mayoría de los casos, el transportador ni siquiera es consciente de que está transportando a otro ser vivo.</li><li><strong>Inquilinismo</strong>: el inquilinismo ocurre cuando una especie ocupa el cuerpo de otra para vivir, sin suponer ningún tipo de molestia.</li><li><strong>Metabiosis</strong>: este tipo de comensalismo es muy común en el reino animal. Se da cuando una especie se alimenta de los desperdicios de otra, como sus heces o el propio cuerpo en descomposición o el caso de los microorganismos anaerobios que comentamos antes.</li></ul>
