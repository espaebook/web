<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_2">Ejemplos de comensalismo </h2>
<div class="apartado">
<p>Existen multitud de relaciones comensalistas en el reino animal. Muchas de estas relaciones se dan entre miembros de este reino con organismos del reino vegetal. Algunos de estos ejemplos son:</p><h3><b>1. Comensalismo entre opiliones y hormigas</b></h3><p>En ciertas zonas de Argentina, donde se ha descubierto esta relación, el clima es muy seco, lo que imposibilita la presencia de <strong>opiliones</strong>, unos animales sociales pertenecientes al orden de los arácnidos. Los hormigueros ofrecen un microclima más húmedo que favorece a los opiliones. Éstos <strong>viven dentro de los hormigueros</strong> sin causar a las hormigas beneficios o perjuicios.</p><h3><b>2. Comensalismo entre el lagarto gigante de la isla del Hierro (<i>Gallotia simonyi</i>) y la gaviota patiamarilla (<i>Larus michahellis</i>)</b></h3><p>Los pollos no volantones de esta especie de gaviota regurgita parte de la comida cuando se sienten muy llenos o son molestados por otras gaviotas adultas. Entonces, el lagarto gigante se beneficia <strong>alimentándose de los insectos regurgitados</strong> por el pollo de gaviota.</p><h3><b>3. Comensalismo entre pinzones vulgares (<i>Fringilla coelebs</i>) y estorninos negros (<i>Sturnus unicolor</i>)</b></h3><p>Los estorninos, en León, al noroeste de España, se alimentan en verano de moras. Cuando comen dejan caer semillas al suelo o sobre las hojas de la morera. Los pinzones, animales granívoros, buscan entre las hojas y el suelo, las <strong>semillas desechadas por los estorninos</strong>, tomándolas incluso directamente de las heces de los estorninos.</p><h3><b>4. Comensalismo entre moscas y ácaros del jamón</b></h3><p>Este es un ejemplo muy curioso de <b>foresis</b>. En los secaderos de jamón, a veces tienen problemas con los ácaros, que pican el jamón y lo invalidan para la venta. Los jamones están colgados en el techo por lo que la invasión por ácaros parecería complicada. Estos animales <strong>se montan en las moscas</strong> que van visitando los jamones. Cuando llegan a un jamón, los ácaros se bajan de la mosca. Las moscas no obtienen nada, ni siquiera se dan cuenta de que están transportando ácaros.</p><h3><b>5. Comensalismo entre aves y árboles</b></h3><p>Las aves que <strong>anidan en los árboles</strong>, obtienen de éste protección y un lugar donde construir su nido. Los árboles no sacan nada, ni positivo ni negativo.</p>
