<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_2">¿Los peces tienen pulmones? </h2>
<div class="apartado">
<p>Aunque pueda parecer contradictorio, la evolución ha provocado la aparición de dipnoos o <b>peces pulmonados</b>. Dentro de la filogenia, están clasificados en la clase Sarcopterygii, por poseer aletas lobuladas. Se cree que estos peces con pulmones están estrechamente relacionados con aquellos primeros peces que dieron lugar a los animales terrestres. Sólo existen seis especies conocidas de peces pulmonados y sólo sobre algunas de ellas tenemos conocimientos sobre su estado de conservación. Algunas ni siquiera poseen nombre común.</p><p>Las <strong>especies de peces con pulmones</strong> son:</p><ul><li>Pez de barro americano (<i>Lepidosiren paradoxa</i>)</li><li>Pez pulmonado africano (<i>Protopterus annectens)</i></li><li><i>Pez pulmonado de mármol (Protopterus aethiopicus)</i></li><li><i>Protopterus amphibius</i></li><li><i>Protopterus dolloi</i></li><li>Pez pulmonado de Queensland o australiano (<i>Neoceratodus forsteri</i>)</li></ul><p>A pesar de poder respirar aire, estos peces están <b>muy ligados al agua</b>, incluso cuando ésta escasea por las sequías, se esconden en el barro protegiendo su cuerpo con una capa de moco que pueden fabricar. Su piel es muy <strong>sensible a la desecación</strong>, por lo que sin esta estrategia morirían.</p>
