<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_1">Sistema respiratorio de los peces </h2>
<div class="apartado">
<p>El sistema respiratorio de los peces recibe el nombre de <strong>bomba buco-opercular</strong>. La primera bomba, la bucal, ejerce presión positiva, por lo que envía el agua hacia la cavidad opercular y, a su vez, esta cavidad mediante presión negativa, succiona el agua de la cavidad bucal. En resumen, la cavidad bucal empuja el agua hacia la cavidad opercular y ésta la succiona.</p><p>Durante una respiración, el pez <strong>abre la boca</strong> y la zona donde está la lengua baja, haciendo que entre más agua, porque la presión disminuye y el agua entra del mar a la boca a favor de gradiente. Después <strong>cierra la la boca</strong> y el suelo bucal sube aumentando la presión y provocando que el agua pase hacia la cavidad opercular, donde la presión es menor.</p><p>Entonces, la cavidad opercular se contrae, obligando al agua a pasar por las branquias donde se producirá el <strong>intercambio gaseoso</strong> y saliendo de forma pasiva por el opérculo. Al abrir el pez la boca de nuevo, puede producirse cierto retorno de agua.</p>
