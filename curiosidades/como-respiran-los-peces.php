<h2 class="titulo titulo--h2 titulo--apartado" id="anchor_0" style="width: 610.88px;">Las branquias de los peces </h2>
<div class="apartado">
<p>Las <strong>branquias</strong> de los peces teleósteos, que son la mayoría de los peces a excepción de tiburones, rayas, lampreas y mixinos, se encuentran a <strong>ambos lados de la cabeza</strong>. Desde fuera podemos ver la cavidad opercular, que es la parte de la "cara del pez" que se abre hacia fuera y se denomina opérculo. Dentro de cada cavidad opercular, encontramos las branquias.</p><p>Las branquias están estructuralmente soportadas por los <strong>arcos branquiales</strong>, que son cuatro. De cada arco branquial, salen dos grupos de filamentos llamados filamentos branquiales que se disponen en forma de "V" con respecto al arco. Cada filamento se superpone con los filamentos vecinos, formando un entramado. A su vez, estos <strong>filamentos branquiales</strong> tienen sus propias proyecciones llamadas <strong>lamelas secundarias</strong>. Aquí se produce el intercambio gaseoso, los peces toman oxígeno y liberan dióxido de carbono.</p><p>El pez toma agua por la boca y, a través de un proceso complejo, libera el agua a través de los opérculos, pasando previamente por las lamelas, donde <strong>se capta el oxígeno</strong>.</p>
